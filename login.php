                            session_start();
                            
                            $_SESSION["loggedin"] = true;
                            $_SESSION["id"] = $id;
                            $_SESSION["username"] = $username;   
                            $_SESSION["role"] = $role;
                            $_SESSION["success"] = "Logged in successfully";

